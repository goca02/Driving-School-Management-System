    private void popuniPlanoveCasa() {
        try {
            ArrayList<PlanCasa> planoviCasa = ClientController.getInstance().getAllPlanCasa();

            cmbPlanCasa.removeAllItems();

            for (PlanCasa planCasa : planoviCasa) {
                cmbPlanCasa.addItem(planCasa);
            }

        } catch (Exception ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

