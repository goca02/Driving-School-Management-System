    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovu evidenciju?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteEvidencijaVoznje(ev);
                FormPretragaEvidencijeVoznje fp = (FormPretragaEvidencijeVoznje) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisana evidencija.");
                this.dispose();
            } catch (Exception ex) {
                Logger.getLogger(FormDetaljiEvidencijeVoznje.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        if (txtBrojCasova.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti broj casova!");
            return;
        }

        try {

            TableModelStavkeEvidencije tm = (TableModelStavkeEvidencije) tblStavke.getModel();
            boolean polozeno = cbPolozeno.isSelected();
            boolean zavrseno = cbZavrseno.isSelected();

            ev.setPolozeno(polozeno);
            ev.setZavrseno(zavrseno);
            ev.setStavkeEvidencije(tm.getLista());

            ClientController.getInstance().updateEvidencijaVoznje(ev);
            FormPretragaEvidencijeVoznje fp = (FormPretragaEvidencijeVoznje) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjena evidencija.");
            this.dispose();

        } catch (Exception ex) {
            Logger.getLogger(FormDetaljiEvidencijeVoznje.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnIzmeniActionPerformed

    private void btnIzracunajCenuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzracunajCenuActionPerformed

        if (txtCenaPoSatu.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Morate uneti cenu casa!");
            return;
        }

        PlanCasa pc = (PlanCasa) cmbPlanCasa.getSelectedItem();
        double cenaPoSatu = Double.parseDouble(txtCenaPoSatu.getText());

        if (cenaPoSatu < 1000 || cenaPoSatu > 2000) {
            JOptionPane.showMessageDialog(this, "Cena po satu mora biti izmedju 1000din i 2000din!");
            return;
        }

        txtCenaCasa.setText(String.valueOf(pc.getTrajanjeUSatima() * cenaPoSatu));
    }//GEN-LAST:event_btnIzracunajCenuActionPerformed

    private void btnDodajStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajStavkuActionPerformed

        try {
            if (txtDatumCasa.getText().isEmpty() || txtCenaCasa.getText().isEmpty()
                    || txtCenaPoSatu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Morate uneti datum i cenu casa!");
                return;
            }

            PlanCasa pc = (PlanCasa) cmbPlanCasa.getSelectedItem();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy");
            Date datumCasa = sdf.parse(txtDatumCasa.getText());
            String tipVoznje = (String) cmbTipVoznje.getSelectedItem();
            double cenaCasa = Double.parseDouble(txtCenaCasa.getText());
            double cenaPoSatu = Double.parseDouble(txtCenaPoSatu.getText());

            if (!datumCasa.after(new Date())) {
                JOptionPane.showMessageDialog(this, "Datum casa mora biti posle danasnjeg dana!");
                return;
            }

            StavkaEvidencije se = new StavkaEvidencije(ev, -1, datumCasa,
                    pc.getTrajanjeUSatima(), tipVoznje, cenaCasa, cenaPoSatu, pc);

            TableModelStavkeEvidencije tm = (TableModelStavkeEvidencije) tblStavke.getModel();
            tm.dodajStavku(se);

            ukupanIznos = tm.vratiUkupanIznos();
            txtUkupanIznos.setText(String.valueOf(ukupanIznos) + "din");

        } catch (ParseException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDodajStavkuActionPerformed

    private void btnObrisiStavkuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiStavkuActionPerformed

        int row = tblStavke.getSelectedRow();

        if (row >= 0) {
            TableModelStavkeEvidencije tm = (TableModelStavkeEvidencije) tblStavke.getModel();
            tm.obrisiStavku(row);
            ukupanIznos = tm.vratiUkupanIznos();
            txtUkupanIznos.setText(String.valueOf(ukupanIznos) + "din");
        }
    }//GEN-LAST:event_btnObrisiStavkuActionPerformed

