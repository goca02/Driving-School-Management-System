    private void popuniKategorije() {
        try {
            ArrayList<KategorijaVozackeDozvole> kategorije = ClientController.getInstance().getAllKategorija();

            cmbKategorija.removeAllItems();

            for (KategorijaVozackeDozvole kategorijaVozackeDozvole : kategorije) {
                cmbKategorija.addItem(kategorijaVozackeDozvole);
            }
        } catch (Exception ex) {
            Logger.getLogger(FormNoviUcenik.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

