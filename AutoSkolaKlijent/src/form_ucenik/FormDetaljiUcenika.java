    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        int result = JOptionPane.showConfirmDialog(this, "Da li ste sigurni da zelite da "
                + "obrisete ovog ucenika?", "Konfirmacija", JOptionPane.YES_NO_OPTION);

        if (result == JOptionPane.NO_OPTION) {
            return;
        }

        if (result == JOptionPane.YES_OPTION) {
            try {
                ClientController.getInstance().deleteUcenik(u);
                FormPretragaUcenika fp = (FormPretragaUcenika) getParent();
                fp.refreshTable();
                JOptionPane.showMessageDialog(this, "Uspesno obrisan ucenik.");
                this.dispose();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Ne mozete da obrisete ovog ucenika jer "
                        + "je bio na casovima!");

            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

    private void btnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzmeniActionPerformed

        try {

            if (txtIme.getText().isEmpty() || txtPrezime.getText().isEmpty()
                    || txtEmail.getText().isEmpty()
                    || txtTelefon.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Sva polja moraju biti popunjena!");
                return;
            }

            String email = txtEmail.getText();
            String telefon = txtTelefon.getText();
            KategorijaVozackeDozvole kategorijaVozackeDozvole = (KategorijaVozackeDozvole) cmbKategorija.getSelectedItem();

            u.setEmail(email);
            u.setTelefon(telefon);
            u.setKategorijaVozackeDozvole(kategorijaVozackeDozvole);

            ClientController.getInstance().updateUcenik(u);
            FormPretragaUcenika fp = (FormPretragaUcenika) getParent();
            fp.refreshTable();
            JOptionPane.showMessageDialog(this, "Uspesno izmenjen ucenik.");
            this.dispose();

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Plan casa nije izmenjen!");

        }

    }//GEN-LAST:event_btnIzmeniActionPerformed


